package com.roze.faana.java.escapeSequence;

public class EscapeSequence {
    public static void main(String[] args) {
        //new line-(backslash- \n)
        System.out.print("Hello\n");
        System.out.println("Java");

       //for tab(backslash-\t)-more spaces
        System.out.print("\tWelcome\n");
        System.out.println("In Depth");

        //print text in double quotes
        System.out.print("\"Spring\"\n");
        System.out.println("Boot");

        //want too print backslash
        System.out.print("\\Firoze\n");
        System.out.println("Hossain");
    }
}
